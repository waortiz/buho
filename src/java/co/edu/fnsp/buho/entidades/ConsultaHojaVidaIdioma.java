package co.edu.fnsp.buho.entidades;

/**
 *
 * @author William
 */
public class ConsultaHojaVidaIdioma {
    private String idioma;
    private String tipoCertificacion;
    private String puntajeInicial;
    private String puntajeFinal;

    /**
     * @return the idioma
     */
    public String getIdioma() {
        return idioma;
    }

    /**
     * @param idioma the idioma to set
     */
    public void setIdioma(String idioma) {
        this.idioma = idioma;
    }

    /**
     * @return the tipoCertificacion
     */
    public String getTipoCertificacion() {
        return tipoCertificacion;
    }

    /**
     * @param tipoCertificacion the tipoCertificacion to set
     */
    public void setTipoCertificacion(String tipoCertificacion) {
        this.tipoCertificacion = tipoCertificacion;
    }

    /**
     * @return the puntajeInicial
     */
    public String getPuntajeInicial() {
        return puntajeInicial;
    }

    /**
     * @param puntajeInicial the puntajeInicial to set
     */
    public void setPuntajeInicial(String puntajeInicial) {
        this.puntajeInicial = puntajeInicial;
    }

    /**
     * @return the puntajeFinal
     */
    public String getPuntajeFinal() {
        return puntajeFinal;
    }

    /**
     * @param puntajeFinal the puntajeFinal to set
     */
    public void setPuntajeFinal(String puntajeFinal) {
        this.puntajeFinal = puntajeFinal;
    }
    
}
