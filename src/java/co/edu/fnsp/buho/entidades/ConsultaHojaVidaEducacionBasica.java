public class ConsultaHojaVidaEducacionBasica {
    private String nivelEstudio;
    private String anyoGraduacionInicial;
    private String anyoGraduacionFinal;

    /**
     * @return the nivelEstudio
     */
    public String getNivelEstudio() {
        return nivelEstudio;
    }

    /**
     * @param nivelEstudio the nivelEstudio to set
     */
    public void setNivelEstudio(String nivelEstudio) {
        this.nivelEstudio = nivelEstudio;
    }

    /**
     * @return the anyoGraduacionInicial
     */
    public String getAnyoGraduacionInicial() {
        return anyoGraduacionInicial;
    }

    /**
     * @param anyoGraduacionInicial the anyoGraduacionInicial to set
     */
    public void setAnyoGraduacionInicial(String anyoGraduacionInicial) {
        this.anyoGraduacionInicial = anyoGraduacionInicial;
    }

    /**
     * @return the anyoGraduacionFinal
     */
    public String getAnyoGraduacionFinal() {
        return anyoGraduacionFinal;
    }

    /**
     * @param anyoGraduacionFinal the anyoGraduacionFinal to set
     */
    public void setAnyoGraduacionFinal(String anyoGraduacionFinal) {
        this.anyoGraduacionFinal = anyoGraduacionFinal;
    }
    
    
}
